package meeting_room;

public class RoomBean {
	
	private String room;
	private int rperson;
	private int rprice;
	private int radd;
	
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	public int getPerson() {
		return rperson;
	}
	public void setPerson(int person) {
		this.rperson = person;
	}
	public int getPrice() {
		return rprice;
	}
	public void setPrice(int price) {
		this.rprice = price;
	}
	public int getAdd() {
		return radd;
	}
	public void setAdd(int add) {
		this.radd = add;
	}
	
}